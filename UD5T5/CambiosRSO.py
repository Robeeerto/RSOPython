def euro_a_libra(euros):
    return euros * 0.83

def libra_a_euro(libras):
    return libras / 0.83

def euro_a_yen(euros):
    return euros * 158.60

def yen_a_euro(yenes):
    return yenes / 158.60

def euro_a_dollar(euros):
    return euros * 1.04

def dollar_a_euro(dolares):
    return dolares / 1.04